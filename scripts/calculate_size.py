# One gigabyte (GB) is equal to 1024 megabytes (MB).

# Calculate the percentage
percentage = (100 / 1024) * 100

# Print the result
print(f"100 MB is approximately {percentage:.2f}% of 1 GB.")


# One gigabyte (GB) is equal to 1024 megabytes (MB).

# Calculate 1% of 1 GB
one_percent = (1 / 100) * 1024

